// 3. Dynamic SEO and Schema Injector
window.updateSEO = function(title, description, canonicalPath, schemaObj) {
  document.title = title;
  
  let descMeta = document.querySelector('meta[name="description"]');
  if (!descMeta) {
    descMeta = document.createElement('meta');
    descMeta.setAttribute('name', 'description');
    document.head.appendChild(descMeta);
  }
  descMeta.setAttribute('content', description);
  
  let canonicalLink = document.querySelector('link[rel="canonical"]');
  if (!canonicalLink) {
    canonicalLink = document.createElement('link');
    canonicalLink.setAttribute('rel', 'canonical');
    document.head.appendChild(canonicalLink);
  }
  const fullCanonicalUrl = window.location.origin + window.location.pathname + (canonicalPath || '');
  canonicalLink.setAttribute('href', fullCanonicalUrl);
  
  // Inject/Replace JSON-LD Schema
  const existingSchema = document.getElementById('seo-jsonld');
  if (existingSchema) {
    existingSchema.remove();
  }
  if (schemaObj) {
    const script = document.createElement('script');
    script.setAttribute('type', 'application/ld+json');
    script.setAttribute('id', 'seo-jsonld');
    script.textContent = JSON.stringify(schemaObj);
    document.head.appendChild(script);
  }
};

