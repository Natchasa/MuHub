// 5. Article Details Modal Actions
window.openArticleModal = function(articleId) {
  const article = ARTICLES_DB[articleId];
  if (!article) return;

  // Set Content
  document.getElementById('articleModalImage').src = article.image;
  document.getElementById('articleModalTitle').textContent = article.title;
  const authorEl = document.getElementById('articleModalAuthor');
  if (authorEl) authorEl.textContent = article.author;
  document.getElementById('articleModalTime').textContent = article.date;
  document.getElementById('articleModalTime').setAttribute('datetime', article.datetime);
  document.getElementById('articleModalReadTime').textContent = article.readTime;
  const ctaHtml = `
    <div class="blog-cta-card">
      <h4>🔮 หนุนดวงชะตาเฉพาะบุคคลกับ MuHub</h4>
      <p>วิเคราะห์ดวงชะตารายวันและบูชาของมงคลที่คำนวณจากลัคนาราศีเกิดของคุณโดยเฉพาะเพื่อหนุนนำสิริมงคล</p>
      <div class="blog-cta-actions">
        <button onclick="switchTab('shop-tab'); closeArticleModal(event);" class="btn btn-coral">🛍️ ไปที่มูมาร์เก็ต</button>
        <button onclick="switchTab('booking-tab'); closeArticleModal(event);" class="btn btn-gold">📅 จองคิวตรวจดวง</button>
      </div>
    </div>
  `;
  document.getElementById('articleModalContent').innerHTML = article.content + ctaHtml;

  // Display Modal
  document.getElementById('articleModalOverlay').style.display = 'flex';
  document.body.style.overflow = 'hidden'; // prevent background scrolling

  // Update hash route without reloading
  window.history.pushState(null, null, '#article/' + articleId);

  // Generate BlogPosting JSON-LD Schema
  const blogSchema = {
    "@context": "https://schema.org",
    "@type": "BlogPosting",
    "headline": article.title,
    "description": article.desc,
    "datePublished": article.datetime,
    "author": {
      "@type": "Person",
      "name": article.author
    },
    "publisher": {
      "@type": "Organization",
      "name": "MuHub",
      "logo": {
        "@type": "ImageObject",
        "url": window.location.origin + "/logo.png"
      }
    },
    "image": [
      article.image
    ]
  };

  // Run SEO Meta Updates
  window.updateSEO(
    article.title + " | มูสเปซ MuHub",
    article.desc,
    '#article/' + articleId,
    blogSchema
  );
};

window.closeArticleModal = function(event) {
  if (event) event.preventDefault();
  
  document.getElementById('articleModalOverlay').style.display = 'none';
  document.body.style.overflow = 'auto'; // restore scrolling

  // Restore Hash to Community
  window.history.pushState(null, null, '#community');

  // Restore Community Tab SEO & FAQ Schema
  window.injectCommunityFAQSchema();
};

// 6. Router based on URL Hash
window.handleHashRouting = function() {
  const hash = window.location.hash;
  if (!hash) return;

  if (hash.startsWith('#article/')) {
    const articleId = hash.replace('#article/', '');
    // switch to community tab first
    if (window.switchTab) {
      window.switchTab('community-tab');
    }
    // open the article modal
    setTimeout(() => {
      window.openArticleModal(articleId);
    }, 100);
  } else if (hash === '#community') {
    if (window.switchTab) window.switchTab('community-tab');
  } else if (hash === '#calculator') {
    if (window.switchTab) window.switchTab('calculator-tab');
  } else if (hash === '#booking') {
    if (window.switchTab) window.switchTab('booking-tab');
  } else if (hash === '#free-horoscope') {
    if (window.switchTab) window.switchTab('my-bookings-tab');
  } else if (hash === '#mu-market') {
    if (window.switchTab) window.switchTab('shop-tab');
  }
};

// Update SEO tags when switching tabs
window.updateTabSEO = function(tabId) {
  const template = TAB_SEO_TEMPLATES[tabId];
  if (!template) return;

  // If community tab, inject FAQ schema
  if (tabId === 'community-tab') {
    window.injectCommunityFAQSchema();
  } else {
    // Inject generic page schema
    const pageSchema = {
      "@context": "https://schema.org",
      "@type": "WebPage",
      "name": template.title,
      "description": template.desc
    };
    window.updateSEO(template.title, template.desc, template.hash, pageSchema);
  }

  // Update hash route silently
  window.history.pushState(null, null, template.hash);
};

// Listen for window load and hash change
window.addEventListener('hashchange', window.handleHashRouting);
window.addEventListener('DOMContentLoaded', () => {
  // Run initial routing on load
  setTimeout(window.handleHashRouting, 200);
});

// 7. Dynamic Article Search and Tag Filtering logic
let activeSelectedTag = 'all';

window.selectCommunityTag = function(tag, btn) {
  activeSelectedTag = tag;
  const pills = document.getElementById('communityTagPills');
  if (pills) {
    pills.querySelectorAll('button').forEach(b => b.classList.remove('active'));
  }
  if (btn) btn.classList.add('active');
  window.filterCommunityArticles();
};

window.filterCommunityArticles = function() {
  const query = document.getElementById('communitySearchInput').value.toLowerCase().trim();
  const cards = document.querySelectorAll('.blog-card');

  cards.forEach(card => {
    const tagEl = card.querySelector('.blog-tag');
    const titleEl = card.querySelector('.blog-title');
    const descEl = card.querySelector('.blog-desc');

    const cardTag = tagEl ? tagEl.textContent.trim() : '';
    const title = titleEl ? titleEl.textContent.toLowerCase() : '';
    const desc = descEl ? descEl.textContent.toLowerCase() : '';

    const tagMatch = (activeSelectedTag === 'all' || cardTag === activeSelectedTag);
    const searchMatch = (!query || title.includes(query) || desc.includes(query));

    if (tagMatch && searchMatch) {
      card.style.display = 'block';
    } else {
      card.style.display = 'none';
    }
  });
};
